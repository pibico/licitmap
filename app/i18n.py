# ─── Public helpers ─────────────────────────────────────────────────────
def t(key: str, lang: str = DEFAULT_LANG, **kwargs: Any) -> str:
    value = TRANSLATIONS.get(lang, {}).get(key)
    if value is None:
        value = TRANSLATIONS[DEFAULT_LANG].get(key, key)
    if kwargs:
        try:
            return value % kwargs
        except (KeyError, TypeError, ValueError):
            return value
    return value


_PLACEHOLDER_RE = re.compile(r"\{\{t\.([a-zA-Z0-9_.]+)\}\}")


def translate_html(html: str, lang: str) -> str:
    """Replace every ``{{t.key}}`` occurrence with its translation."""
    return _PLACEHOLDER_RE.sub(lambda m: t(m.group(1), lang), html)


def get_lang_from_request(request: Request) -> str:
    cookie = request.cookies.get("lm_lang")
    if cookie in SUPPORTED:
        return cookie
    accept = request.headers.get("accept-language", "")
    head = accept[:2].lower()
    if head in SUPPORTED:
        return head
    return DEFAULT_LANG


# ─── Middleware ─────────────────────────────────────────────────────────
class I18nMiddleware(BaseHTTPMiddleware):
    """Post-processes HTML responses, replacing ``{{t.key}}`` placeholders."""

    async def dispatch(self, request, call_next):
        response: Response = await call_next(request)
        content_type = response.headers.get("content-type", "")
        if not content_type.startswith("text/html"):
            return response
        if response.status_code >= 300:
            return response

        body = b""
        async for chunk in response.body_iterator:
            body += chunk
        try:
            text = body.decode("utf-8")
        except UnicodeDecodeError:
            return Response(
                content=body,
                status_code=response.status_code,
                headers=dict(response.headers),
                media_type=content_type,
            )

        if "{{t." not in text:
            return Response(
                content=body,
                status_code=response.status_code,
                headers=dict(response.headers),
                media_type=content_type,
            )

        lang = get_lang_from_request(request)
        translated = translate_html(text, lang).encode("utf-8")
        headers = {k: v for k, v in response.headers.items() if k.lower() != "content-length"}
        return Response(
            content=translated,
            status_code=response.status_code,
            headers=headers,
            media_type=content_type,
        )
